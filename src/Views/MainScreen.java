    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ProductDao p = new ProductDao();
        ArrayList<Product> products = p.GetAllProducts();
        Object[] tableColumnNames;
        tableColumnNames = new Object[5];
        tableColumnNames[0] = "Product_ID";
        tableColumnNames[1] = "Type";
        tableColumnNames[2] = "Manufacturer";
        tableColumnNames[3] = "Production_Date";
        tableColumnNames[4] = "Expiry_Date";

        DefaultTableModel dtm;
        dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(tableColumnNames);
        this.jTable1.setModel(dtm);
        Object[] RowProduct;
        RowProduct = new Object[5];

        for (int i = 0; i < products.size(); i++) {
            RowProduct[0] = products.get(i).getProduct_ID();
            RowProduct[1] = products.get(i).getType();
            RowProduct[2] = products.get(i).getManufacturer();
            RowProduct[3] = products.get(i).getProduction_Date();
            RowProduct[4] = products.get(i).getExpiry_Date();
            dtm.addRow(RowProduct);

        }


    }//GEN-LAST:event_jButton1ActionPerformed

