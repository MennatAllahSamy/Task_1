    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int Product_ID = Integer.parseInt(this.txtID.getText());
        String Type = this.txtType.getText();
        String Manufacturer = this.txtManu.getText();
        String Production_Date = this.txtProduction.getText();
        String Expiry_Date = this.txtExpiry.getText();
        Product product = new Product();
        product.setProduct_ID(Product_ID);
        product.setType(Type);
        product.setManufacturer(Manufacturer);
        product.setProduction_Date(Production_Date);
        product.setExpiry_Date(Expiry_Date);

        ProductDao dao = new ProductDao();
        int result = dao.InsertProduct(product);
        if (result != 0) {
            JOptionPane.showMessageDialog(null, "Product Added!");
        } else {
            JOptionPane.showMessageDialog(null, "Failed!");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

